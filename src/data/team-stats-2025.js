// Get all teams sorted by projected strength
export function getTeamRankings2025() {
  return Object.entries(teamStats2025)
    .map(([id, stats]) => ({
      teamId: parseInt(id),
      ...stats,
      strengthRating: (stats.avgPointsScored - stats.avgPointsAllowed) / stats.defensiveRating
    }))
    .sort((a, b) => b.strengthRating - a.strengthRating);
}

